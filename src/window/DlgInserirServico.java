    private void tblItensMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblItensMouseClicked
        Produto itemSelecionado = null;
        int linha = tblItens.getSelectedRow();
        itemSelecionado = (Produto) tblItens.getValueAt(linha, 1); 
        gerIG.abrirEditarServico(itemSelecionado);
        carregarTabela();
    }//GEN-LAST:event_tblItensMouseClicked

    public boolean validarDadosInserir() {
        lblNomeServico.setForeground(Color.black);
        lblValorCusto.setForeground(Color.black);
        lblValorVenda.setForeground(Color.black);
        lblQuatidade.setForeground(Color.black);

        String msgErro = "";

        if (txtNomeServico.getText().isEmpty()) {
            msgErro = msgErro + "Digite o nome do Serviço!\n";
            lblNomeServico.setForeground(Color.red);
        }
        if (txtQuantidade.getText().isEmpty()) {
            msgErro = msgErro + "Digite quantidade em Estoque!\n";
            lblQuatidade.setForeground(Color.red);
        }
        if (txtValorVenda.getText().isEmpty()) {
            msgErro = msgErro + "Digite o valor de Venda!\n";
            lblValorVenda.setForeground(Color.red);
        }

        if (txtValorCusto.getText().isEmpty()) {
            msgErro = msgErro + "Digite o valor de Custo\n";
            lblValorCusto.setForeground(Color.red);
        }
        if (msgErro.isEmpty()) {
            return true;
        } else {
            JOptionPane.showMessageDialog(this, msgErro + "Favor Completar os campos!", "Inserir Serviço", JOptionPane.ERROR_MESSAGE);
            return false;
        }
    }

    public void carregarTabela() {
        ((DefaultTableModel) tblItens.getModel()).setRowCount(0);
        String nome = txtBuscaProduto.getText();
        List<Produto> listaServico = null;
        try {
            listaServico = (List<Produto>) gerIG.getGerDom().carregarItem(0, nome);
            if (!listaServico.isEmpty()) {
                for (Produto item : listaServico) {
                    ((DefaultTableModel) tblItens.getModel()).addRow(item.toArray());
                }
            }
        } catch (Exception ex) {
            Logger.getLogger(DlgMostrarCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
