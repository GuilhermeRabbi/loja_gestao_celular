    private void btnInserirProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirProdutoActionPerformed

        String nome = txtNomeServico.getText();
        String valores = txtValorCusto.getText();
        Produto produto = null;
        List<Produto> listaProduto = null;

        if (validarDadosInserir()) {
            try {
                Double valorCusto = Double.parseDouble(txtValorCusto.getText());
                Double valorVenda = Double.parseDouble(txtValorVenda.getText());
                int qtd_estoque = Integer.parseInt(txtQuantidade.getText());
                listaProduto = (List<Produto>) gerIG.getGerDom().carregarItem(1, nome);
                if(listaProduto.isEmpty()){
                    
                    produto = new Produto(0, nome, valorCusto, valorVenda, qtd_estoque);
                    if (gerIG.inserirItem(produto)) {
                        String mensagem = produto.getNome();
                        JOptionPane.showMessageDialog(this, "Produto Inserido\n" + mensagem);
                        ((DefaultTableModel) tblItens.getModel()).addRow(produto.toArray());   
                    }
                }else{
                    JOptionPane.showMessageDialog(this, "Produto já Existente\n");
                }

            } catch (Exception ex) {
                Logger.getLogger(DlgItem.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnInserirProdutoActionPerformed

